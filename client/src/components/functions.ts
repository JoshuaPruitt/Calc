const add = (input1: number, input2: number) => {
    return input1 + input2;
};

const mult = (input1: number, input2: number) => {
    return input1 * input2;
};

const sub = (input1: number, input2: number) => {
    return input1 - input2;
};

const div = (input1: number, input2: number) => {
    return input1 / input2;
};


export {add, mult, sub, div};